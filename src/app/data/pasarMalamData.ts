export type EventRegion = 'Eastern Singapore' | 'Northern Singapore' | 'Western Singapore' | 'Central Singapore' | 'City & CBD';
export type EventStatus = 'ongoing' | 'upcoming' | 'completed';

export interface PasarMalamEvent {
  id: string;
  name: string;
  region: EventRegion;
  area: string;
  startDate: string; // YYYY-MM-DD
  endDate: string;   // YYYY-MM-DD
  openingHours: string;
  venue: string;
  address: string;
  nearestMrt: string;
  description: string;
  featured?: boolean;
}

/** Compute status dynamically from today's date — no hardcoded status. */
export function getEventStatus(event: PasarMalamEvent): EventStatus {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const start = new Date(event.startDate);
  const end = new Date(event.endDate);
  end.setHours(23, 59, 59, 999);
  if (today > end) return 'completed';
  if (today < start) return 'upcoming';
  return 'ongoing';
}

